package frc.robot.commands.auto;

import edu.wpi.first.wpilibj2.command.Command;
import frc.robot.RobotState;
import frc.robot.RobotState.RobotAction;

public class AutoIntake extends Command {
  @Override
  public void initialize() {
    RobotState.getInstance().updateRobotAction(RobotAction.kAutoCoralIntaking);
  }

  @Override
  public boolean isFinished() {
    // we go from kAutoCoralIntaking to kAutoDefault once the intake is done
    return RobotState.getInstance().getCurrentAction() != RobotAction.kAutoCoralIntaking;
  }
}
